package com.uday.info;

public class DispositionStatus {
	
	private String policyProposalNo;
	
	private String policyProposalFlag;
	
	private String dispostatid;
	
	private String dispostatdesc;

	public String getDispostatid() {
		return dispostatid;
	}

	public void setDispostatid(String dispostatid) {
		this.dispostatid = dispostatid;
	}

	public String getPolicyProposalNo() {
		return policyProposalNo;
	}

	public void setPolicyProposalNo(String policyProposalNo) {
		this.policyProposalNo = policyProposalNo;
	}

	public String getPolicyProposalFlag() {
		return policyProposalFlag;
	}

	public void setPolicyProposalFlag(String policyProposalFlag) {
		this.policyProposalFlag = policyProposalFlag;
	}

	public String getDispostatdesc() {
		return dispostatdesc;
	}

	public void setDispostatdesc(String dispostatdesc) {
		this.dispostatdesc = dispostatdesc;
	}

}
